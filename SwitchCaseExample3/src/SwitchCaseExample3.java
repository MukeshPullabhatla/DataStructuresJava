
public class SwitchCaseExample3 {
	public static void main(String[] args) {
		char ch = 'b';
		switch(ch)
		{
		case 'd':
			System.out.println("case 1: ");
			break;
		case 'b':
			System.out.println("case 2: ");
			break;
		case 'x':
			System.out.println("case 3: ");
			break;
		case 'y':
			System.out.println("case 4: ");
			break;
		default:
			System.out.println("default: ");
		}
		
	}

}
